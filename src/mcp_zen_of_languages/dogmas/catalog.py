def dogmas_for_rule_id(rule_id: str) -> tuple[str, ...]:
    """Return the explicit universal dogmas assigned to a rule id."""
    return RULE_DOGMA_CATALOG.get(rule_id, ())


def dogmas_for_rule_ids(rule_ids: list[str] | tuple[str, ...]) -> tuple[str, ...]:
    """Return ordered unique dogmas for one or more explicit rule ids."""
    ordered: list[str] = []
    seen: set[str] = set()
    for rule_id in rule_ids:
        for dogma in dogmas_for_rule_id(rule_id):
            if dogma in seen:
                continue
            seen.add(dogma)
            ordered.append(dogma)
    return tuple(ordered)


